package com.pl.kasia.services;


import java.io.IOException;
import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;
import java.util.ArrayList;
import java.util.List;
import java.util.Properties;


import studia.Student;




public class StudentDBManager {

	
	private Connection conn;

	private Statement stmt;

	private PreparedStatement DodajStudentStmt;
	private PreparedStatement PobierzStudentStmt;

	private boolean add;

	
	
	public StudentDBManager()
	{
		try {
			

			conn= DriverManager.getConnection("jdbc:hsqldb:hsql://localhost/workdb");

			stmt=conn.createStatement();
			boolean studentTableExists=false;

			ResultSet rs= conn.getMetaData().getTables(null, null, null, null);

			while(rs.next())
			{
				if("Student".equalsIgnoreCase(rs.getString("TABLE_IMIE")))
					{
						studentTableExists=true;
						break;
					}
			}


			if(!studentTableExists)
			{
				stmt.executeUpdate("" +
					"CREATE TABLE student(" +
					"indeks bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY," +
					"imie varchar(20)," +
					"nazwisko varchar(20)" +
					")");
			}

			DodajStudentStmt=conn.prepareStatement("" +
					"INSERT INTO student (name) VALUES (?)" +
					"");

			PobierzStudentStmt=conn.prepareStatement("" +
					"SELECT * FROM student" +
					"");



		} catch (SQLException e) {

			e.printStackTrace();
		}
	}

	public void DodajStudent(Student s)
	{
		try {
			DodajStudentStmt.setString(1, s.getImie());

			DodajStudentStmt.executeUpdate();

		} catch (SQLException e) {

			e.printStackTrace();
		}

	}

	public List<Student> DodajWszytskichStudent()
	{
		List<Student> student=new ArrayList<Student>();

		try {
			ResultSet rs= PobierzStudentStmt.executeQuery();

			while(rs.next())
			{
				
			 student.add(new Student(rs.getString("Imie"), null, null));
			}

		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}

		return student;
	}



	
}
